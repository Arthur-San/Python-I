# Exemplo de tipos de dados em Python

# 1. Tipos numéricos
inteiro = 10
flutuante = 3.14
complexo = 2 + 3j

# 2. Tipos de texto
string = "Olá, mundo!"
caractere = 'A'

# 3. Tipos booleanos
verdadeiro = True
falso = False

# 4. Tipos de sequência
lista = [1, 2, 3, 4, 5]
tupla = (10, 20, 30)
conjunto = {1, 2, 3}
dicionario = {'chave1': 'valor1', 'chave2': 'valor2'}

# 5. Tipos nulos
nulo = None

# Explanations
print("1. Tipos numéricos:")
print(f"inteiro: {inteiro}, tipo: {type(inteiro)}")
print(f"flutuante: {flutuante}, tipo: {type(flutuante)}")
print(f"complexo: {complexo}, tipo: {type(complexo)}")

print("\n2. Tipos de texto:")
print(f"string: {string}, tipo: {type(string)}")
print(f"caractere: {caractere}, tipo: {type(caractere)}")

print("\n3. Tipos booleanos:")
print(f"verdadeiro: {verdadeiro}, tipo: {type(verdadeiro)}")
print(f"falso: {falso}, tipo: {type(falso)}")

print("\n4. Tipos de sequência:")
print(f"lista: {lista}, tipo: {type(lista)}")
print(f"tupla: {tupla}, tipo: {type(tupla)}")
print(f"conjunto: {conjunto}, tipo: {type(conjunto)}")
print(f"dicionario: {dicionario}, tipo: {type(dicionario)}")

print("\n5. Tipos nulos:")
print(f"nulo: {nulo}, tipo: {type(nulo)}")
